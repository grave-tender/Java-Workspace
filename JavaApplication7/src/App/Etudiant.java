// package App;

public class Etudiant {
    String nom, prenom;

    public Etudiant(){

    }

    public void setNom(String unNom) {
        nom = unNom;
    }

    public String getNom() {
        return nom;
    }

    public void setPrenom(String unPrenom) {
        prenom = unPrenom;
    }

    public String getPrenom() {
        return prenom;
    }
}

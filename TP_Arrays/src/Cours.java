public class Cours {
    private String IDCours;
    private String NomCours;

    public Cours(String IDCours, String NomCours) {
        this.IDCours = IDCours;
        this.NomCours = NomCours;
    }

    public String getIDCours() {
        return IDCours;
    }

    public String getNomCours() {
        return NomCours;
    }

}
